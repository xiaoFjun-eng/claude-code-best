import { AGENT_TOOL_NAME } from '@claude-code-best/builtin-tools/tools/AgentTool/constants.js'
import { registerBundledSkill } from '../bundledSkills.js'

const SIMPLIFY_PROMPT = `# 简化：代码审查与清理

审查所有已更改的文件，关注代码复用、质量和效率。修复发现的所有问题。

## 第一阶段：识别变更

运行 \`git diff\`（如果有暂存的更改，则运行 \`git diff HEAD\`）以查看更改内容。如果没有 git 变更，则审查用户在本对话中提及或你之前编辑过的最新修改的文件。

## 第二阶段：并行启动三个审查代理

使用 ${AGENT_TOOL_NAME} 工具在单条消息中同时启动所有三个代理。将完整的 diff 传递给每个代理，使其拥有完整的上下文。

### 代理 1：代码复用审查

针对每项变更：

1.  **搜索现有的工具函数和辅助函数**，以替代新编写的代码。在代码库的其他地方寻找类似的模式——常见位置包括工具目录、共享模块以及更改文件相邻的文件。
2.  **标记任何重复现有功能的新函数**。建议改用现有函数。
3.  **标记任何可以使用现有工具函数的内联逻辑**——手写的字符串操作、手动路径处理、自定义环境检查、临时类型守卫以及类似的模式通常是常见候选。

### 代理 2：代码质量审查

针对相同的变更审查是否存在临时凑合的模式：

1.  **冗余状态**：重复现有状态的状态、可以派生的缓存值、可以改为直接调用的观察者/副作用。
2.  **参数蔓延**：向函数添加新参数，而不是对现有参数进行泛化或重构。
3.  **带有细微差异的复制粘贴**：近乎重复的代码块，应通过共享抽象进行统一。
4.  **抽象泄露**：暴露应封装的内容，或破坏现有的抽象边界。
5.  **字符串类型代码**：在代码库中已存在常量、枚举（字符串联合类型）或品牌类型的地方使用原始字符串。
6.  **不必要的 JSX 嵌套**：不增加布局价值的包装 Box/元素——检查内部组件属性（flexShrink、alignItems 等）是否已提供所需行为。
7.  **不必要的注释**：解释代码**做什么**的注释（命名良好的标识符已经做到了这一点）、叙述变更的注释或引用任务/调用者的注释——删除；只保留非显而易见的**为什么**（隐藏的约束、微妙的不变量、变通方法）。

### 代理 3：效率审查

针对相同的变更审查效率问题：

1.  **不必要的工作**：冗余计算、重复的文件读取、重复的网络/API 调用、N+1 模式。
2.  **错失并发机会**：可以并行运行的独立操作却按顺序运行。
3.  **热点路径膨胀**：在启动或每次请求/每次渲染的热点路径上添加了新的阻塞性工作。
4.  **重复的无操作更新**：轮询循环、间隔或无条件触发的事件处理程序内部的状态/存储更新——添加变更检测守卫，以便在没有任何更改时不会通知下游消费者。另外：如果包装函数接受更新器/归约器回调，请验证它是否遵循相同引用返回（或任何“无更改”信号）——否则调用者的提前返回无操作将悄无声息地失效。
5.  **不必要的存在性检查**：在操作之前预先检查文件/资源是否存在（TOCTOU 反模式）——直接操作并处理错误。
6.  **内存**：无界的数据结构、缺少清理、事件监听器泄漏。
7.  **过于宽泛的操作**：在只需要一部分时读取整个文件，在筛选单个项目时加载所有项目。

## 第三阶段：修复问题

等待所有三个代理完成。汇总它们的发现并直接修复每个问题。如果某个发现是误报或不值得处理，请注明并继续——不要与发现争论，直接跳过即可。

完成后，简要总结修复的内容（或确认代码已经是干净的）。`

export function registerSimplifySkill(): void {
  registerBundledSkill({
    name: 'simplify',
    description:
      '审查已更改的代码，关注复用性、质量和效率，然后修复发现的所有问题。',
    userInvocable: true,
    async getPromptForCommand(args) {
      let prompt = SIMPLIFY_PROMPT
      if (args) {
        prompt += `

## 额外关注点

${args}`
      }
      return [{ type: 'text', text: prompt }]
    },
  })
}
